def meteor_score_safe(pred: str, gold: str) -> Optional[float]:
    """
    METEOR (`meteor_score` from nltk).  Returns None if nltk + the wordnet/omw
    corpora aren't available, so the caller can silently skip when on a
    minimal install.
    """
    try:
        from nltk.translate.meteor_score import meteor_score as _meteor
        # NLTK requires both 'wordnet' and 'omw-1.4'.  We try to download
        # them once on first call; subsequent calls are no-ops.
        import nltk
        for resource in ("wordnet", "omw-1.4"):
            try:
                nltk.data.find(f"corpora/{resource}")
            except LookupError:
                try:
                    nltk.download(resource, quiet=True)
                except Exception:
                    return None
    except Exception:
        return None

    try:
        # NLTK API: takes list of references (each a token list) and
        # one hypothesis (token list).
        gold_toks = normalize_text(gold).split()
        pred_toks = normalize_text(pred).split()
        if not gold_toks or not pred_toks:
            return 0.0
        return float(_meteor([gold_toks], pred_toks))
    except Exception:
        return None


_bertscore_scorer = None  # lazily-built BERTScorer (caches the model on GPU)


def bertscore_corpus(
    preds: list[str],
    golds: list[str],
    model_type: str = "roberta-large",
    lang: str = "en",
    batch_size: int = 32,
    rescale_with_baseline: bool = False,
) -> Optional[list[dict]]:
    """
    Compute BERTScore (precision/recall/F1) over a parallel list of
    predictions and references.  Returns a list of dicts, one per pair,
    or None if the `bert-score` package is unavailable.

    Why corpus-level: BERTScore loads a ~350M-parameter encoder; we never
    want to pay that cost per example.  Caller invokes this once after the
    main eval loop and merges the results into per-example records.

    Defaults
    --------
    * `model_type="roberta-large"` is the default that bert-score ships
      precomputed baseline files for.  DeBERTa-v3 gives slightly better
      correlation with human judgement but its baselines aren't bundled
      and it requires sentencepiece + a working SPM model.
    * `rescale_with_baseline=False` because turning it on requires a
      perfectly matching baseline file for the model+layer combo and any
      mismatch surfaces as opaque "int too big to convert" errors.  The
      raw F1 scores are still meaningful for comparing models on the
      same test set, which is what the report needs.
    """
    if not preds or not golds or len(preds) != len(golds):
        return None
    # bert-score 0.3.13 calls tokenizer.build_inputs_with_special_tokens,
    # which `transformers` >=5.0 removed.  Patch the method back onto the
    # tokenizer base classes so bert-score keeps working.  This is
    # idempotent and safe — the implementation matches what HF historically
    # provided.
    try:
        from transformers import PreTrainedTokenizer, PreTrainedTokenizerFast

        def _build_inputs_with_special_tokens(self, token_ids_0, token_ids_1=None):
            cls = [self.cls_token_id] if self.cls_token_id is not None else []
            sep = [self.sep_token_id] if self.sep_token_id is not None else []
            if token_ids_1 is None:
                return cls + list(token_ids_0) + sep
            return cls + list(token_ids_0) + sep + list(token_ids_1) + sep

        for _cls in (PreTrainedTokenizer, PreTrainedTokenizerFast):
            if not hasattr(_cls, "build_inputs_with_special_tokens"):
                _cls.build_inputs_with_special_tokens = _build_inputs_with_special_tokens  # type: ignore[attr-defined]
    except Exception:
        pass

    try:
        from bert_score import BERTScorer
    except ImportError:
        print("  [bertscore] package not installed (`pip install bert-score`); skipping.")
        return None

    global _bertscore_scorer
    if _bertscore_scorer is None:
        try:
            _bertscore_scorer = BERTScorer(
                model_type=model_type,
                lang=lang,
                rescale_with_baseline=rescale_with_baseline,
            )
        except Exception as e:
            print(f"  [bertscore] failed to load {model_type}: {e}; "
                  "retrying with default lang=en")
            try:
                _bertscore_scorer = BERTScorer(
                    lang=lang, rescale_with_baseline=rescale_with_baseline,
                )
            except Exception as e2:
                print(f"  [bertscore] failed to load any scorer: {e2}")
                return None

    # Replace empty strings with a single space so BERTScore doesn't
    # crash on degenerate generations.
    safe_preds = [p if p.strip() else " " for p in preds]
    safe_golds = [g if g.strip() else " " for g in golds]

    try:
        try:
            P, R, F = _bertscore_scorer.score(safe_preds, safe_golds, batch_size=batch_size)
        except TypeError:
            P, R, F = _bertscore_scorer.score(safe_preds, safe_golds)
    except Exception as e:
        print(f"  [bertscore] scoring error: {e}")
        # Retry once without rescale (most common cause of overflow errors).
        if rescale_with_baseline and _bertscore_scorer.rescale_with_baseline:
            print("  [bertscore] retrying with rescale_with_baseline=False")
            _bertscore_scorer.rescale_with_baseline = False
            try:
                P, R, F = _bertscore_scorer.score(safe_preds, safe_golds)
            except Exception as e2:
                print(f"  [bertscore] retry also failed: {e2}")
                return None
        else:
            return None

    return [
        {"bertscore_p": float(p), "bertscore_r": float(r), "bertscore_f1": float(f)}
        for p, r, f in zip(P.tolist(), R.tolist(), F.tolist())
    ]